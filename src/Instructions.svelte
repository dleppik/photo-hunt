<h1>Photo Scavenger Hunt</h1>

<p>
    Play in teams of two.
</p>
<p>
    Try to take a photo from the same angle as the clue.
    If you can, include your partner (face, hand, foot; whatever fits) in the frame.
</p>
<p>
    Be sneaky to throw off other teams: pretend to take photos in the wrong places, and be quick in the right ones.
</p>
<p>
    Some photos will show you where in the park to find the next clue.
</p>
<p>
    Most important, <b>be respectful</b>. This game is weird enough as it is,
    don't give people any more reason to question your motives.
</p>
<ul>
    <li>
        Don't climb on anything or touch electrical equipment.
    </li>
    <li>Don't get in the way of kids in the playground.
    </li>
    <li>
        Be especially mindful of people paying their respects in the war memorial.
    </li>
</ul>
<p>
    The war memorial is a somber place. It may cause you to have strong feelings.
    <i>Take care of yourself and each other.</i>
</p>

<p>
    And, of course, <b>have fun.</b>
</p>